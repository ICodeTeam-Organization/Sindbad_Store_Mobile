class NotifctionEntity {
  final DateTime notficationDate;
  final String notficationTitle;
  final bool notficationstatus;
  final String orderNumber;
  final int notficationAction;
  final String notficationBody;
  final int notifictionId;
  final int orderId;
  NotifctionEntity({
    required this.notficationDate,
    required this.notficationTitle,
    required this.notficationstatus,
    required this.orderNumber,
    required this.notficationAction,
    required this.notficationBody,
    required this.notifictionId,
    required this.orderId,
  });
}
